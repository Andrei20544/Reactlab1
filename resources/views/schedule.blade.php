                <li class="item"><a href="">Body training (1,2,3) - тренировка с использованием самого различного оборудования направленная на развитие всех мышечных групп. Позволяет достигать комплексного развития физических способностей и успешно содействует повышению общей работоспособности организма (50 мин.)</a></li>
                <li class="item"><a href="">Cross Training (2,3) – интенсивная интервальная тренировка, включающая в себя упражнения cross fit, направленных на развитие: силы, координации, баланса, тренировку мышечной выносливости, кардио-системы и улучшения рельефа мышц. (50 мин)</a></li>
                <li class="item"><a href="">Legs&Butt (1,2,3) - Тренировки по этой программе позволяют повысить силу и выносливость мышц ног и ягодиц, делают ноги более стройными, подтягивают и укрепляют ягодицы, сжигают лишние калории, улучшают кардиовыносливость. Оборудование на усмотрение тренера. (45-50 мин)</a></li>
                <li class="item"><a href="">Fit ball / Fit ball-Power (1,2,3) силовая тренировка с использование фитнес мячей / различного оборудования на усмотрение тренера (45-50 мин)</a></li>
                <li class="item"><a href="">Torso (1,2,3) - урок направлен на укрепление мышц верхнего плечевого пояса (мышцы спины, груди и плеч) (45-50 мин)</a></li>
                <li class="item"><a href="">Core&Butt (1,2,3) - силовая тренировка для укрепления мышц кора а также мышцами ягодиц, (45-50мин)</a></li>
                <li class="item"><a href="">ABS (1,2,3) - силовой класс, направленный на укрепление мышц живота+растяжка (45-50 мин)</a></li>
                <li class="item"><a href="">Step&Legs (2,3) - тренировка на укрепление всех мышц тела, развитие координации и повышение выносливости с использованием степ платформы+ силовая тренировка мышц ног (50 мин.)</a></li>
                <li class="item"><a href="">Step Dance (2,3) - кардиотренировка c элементами танцевальных движений и использованием специальных степ платформ развивает координацию и эффективно сжигает калории (50мин)</a></li>
                <li class="item"><a href="">Step&ABS (1,2,3) - комбинированная тренировка на степ платформах в первой части и комплексом упражнений на пресс во второй части занятия (50 мин)</a></li>
                <li class="item"><a href="">Zumba (1,2,3) - зажигательная танцевальная тренировка, на основе латинских ритмов. С ней вы обретёте великолепную форму и зарядиться позитивом.</a></li>
                <li class="item"><a href="">Core&Stretch (1,2,3) - тренировка средней интенсивности для укрепления мышц кора (низ спины, мышцы ягодиц, мышц пресcа)+ растяжка (45-50 мин)</a></li>
                <li class="item"><a href="">Stretch (1,2,3) - тренировки по программе улучшают гибкость и подвижность суставов, способствует снятию стресса и глубокому расслаблению (50 мин)</a></li>
                <li class="item"><a href="">Port De Bras (1,2,3) - восстановительное и реабилитационное занятие, основанное на функциональном и динамическом вытяжении. Развивает навыки и технику движения, способность гармонично и полноценно использовать возможности своего тела (50 мин.)</a></li>
                <li class="item"><a href="">Balance (1,2,3) - тренировки по данной программе улучшают гибкость и подвижность в суставах, силу мышечного корсета и осанку, успокаивают и понижают уровень стресса, сжигают калории (45-50 мин)</a></li>
                <li class="item"><a href="">Fitness+ (>50) - занятие для людей пожилого возраста, включающее в себя силовые упражнения с легкими весами, упражнения на развитие координации движения, улучшение гибкости и дыхательные упражнения (45 мин)</a></li>
                <li class="item"><a href="">Fitness yoga (1,2,3) - урок направленный на достижение гармонии тела и духа. Поможет развить гибкость, укрепить мышцы и исправить осанку (50 мин)</a></li>
                <li class="item"><a href="">Hatha Yoga (1,2,3) – Проверенная тысячелетиями практика развития личности, через физическое совершенствование тела и внутренней гармонии. Тренировка способствует нормализации психоэмоционального состояния, повышению силы и развитию гибкости. (90 мин.)</a></li>
